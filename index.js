function checkTime(){
    var today = new Date();
    var hr = today.getHours();
    var min = today.getMinutes();
    var sec = today.getSeconds();
    
    if(hr < 10){
      hr = "0" + hr;
    }
      if(min < 10){
      min = "0" + min;
    }
      if(sec < 10){
      sec = "0" + sec;
    }
    date = hr + ':' + min + ":" + sec
    time.innerHTML = date
  }
  
  setInterval(checkTime, 500);


